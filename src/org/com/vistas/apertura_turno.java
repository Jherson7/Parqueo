    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        turno_db turno = new turno_db();
        turno aux = turno.consultar_turno(Controlador.getParqueo().getId_parqueo());
        if(aux!=null&&aux.getHora_cierre()==null){
            Controlador.setTurno_actual(aux);
             JOptionPane.showMessageDialog(null, "Ya existe el turno aperturado, cierre el turno para poder iniciar otro ", "Panel de Usuarios", 0);
             this.dispose();
             return;
        }
        if (txtPassword.getText().equalsIgnoreCase(Controlador.getUsuarioActual().getPassword())) {
            //verificar que no haya un turno abierto antes
            aux = turno.aperturar_turno(Controlador.getUsuarioActual().getIdUSUARIO(), Controlador.getParqueo().getId_parqueo());
            if (aux!= null) {
                JOptionPane.showMessageDialog(null, "Se aperturo exitosamente el turno", "Panel de Usuarios", 1);
                Controlador.setTurno_actual(aux);
                //Controlador.insertar_tickets_pendientes(); descomentar esto
                //si desean guardar los tickets que se generen sin aperturar turno
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Error al aperturar el turno ", "Panel de Usuarios", 0);
            }
            //realizar la apertura del turno
        }else{
            JOptionPane.showMessageDialog(null, "Las contrasenias no coinciden", "Panel de Usuarios", 0);
        }
    }//GEN-LAST:event_jButton1ActionPerformed


